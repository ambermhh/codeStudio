// using arrays
function repeatedNumbers(arr) {
  for (let i = 0; i < arr.length; i++) {
    for (let j = i + 1; j < arr.length; j++) {
      if (arr[i]===(arr[j])) {
        return arr[i];
      }
    }
  }
  return undefined;
} //O(n^2)

// using hash table
function repeatedNumbers2(arr) {
  let map = {};
  for (let i = 0; i < arr.length; i++) {
    if (map[arr[i]] !== undefined) {
      return arr[i];
    } else {
      map[arr[i]] = i;
    }
  }
  return undefined;
} // O(n)

